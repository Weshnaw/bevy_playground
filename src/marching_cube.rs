//     - https://vodacek.zvb.cz/archiv/339.html
fn setup_marched_cube_example(
    mut commands: Commands,
    mut meshes: ResMut<Assets<Mesh>>,
    mut materials: ResMut<Assets<StandardMaterial>>,
) {
    commands.spawn((
        DirectionalLight {
            illuminance: lux::FULL_DAYLIGHT,
            shadows_enabled: true,
            ..default()
        },
        Transform::from_xyz(0., 20., 75.).looking_at(Vec3::new(0., 1., 0.), Vec3::Y),
    ));
    let noise = BasicMulti::<Perlin>::new(1624);

    let calc = |x, y, z| {
        let height = (noise.get([x as f64 / 12., z as f64 / 12.]) * 1.) + 1.;
        y as f64 <= height
    };

    let mut idx = vec![];
    let mesh_handle = meshes.add(Cuboid::from_size(vec3(0.1, 0.1, 0.1)).mesh());
    let material_handle = materials.add(Color::WHITE);
    for x in 0..100 {
        let mut x_idx = vec![];
        for y in 0..100 {
            let mut y_idx = vec![];
            for z in 0..100 {
                let mut idx = 0;
                if calc(x, y, z) {
                    idx |= 0b00000001;
                    commands.spawn((
                        Mesh3d(mesh_handle.clone()),
                        MeshMaterial3d(material_handle.clone()),
                        Transform::from_xyz(x as f32 * 2., y as f32 * 2., z as f32 * 2.),
                        // WireframeObject,
                    ));
                }
                if calc(x, y, z + 1) {
                    idx |= 0b00000010;
                }
                if calc(x + 1, y, z + 1) {
                    idx |= 0b00000100;
                }
                if calc(x + 1, y, z) {
                    idx |= 0b00001000;
                }
                if calc(x, y + 1, z) {
                    idx |= 0b00010000;
                }
                if calc(x, y + 1, z + 1) {
                    idx |= 0b00100000;
                }
                if calc(x + 1, y + 1, z + 1) {
                    idx |= 0b01000000; // 0,0,0
                }
                if calc(x + 1, y + 1, z) {
                    idx |= 0b10000000;
                }
                y_idx.push(idx);
                // 0b10000000 -> 1,1,0
                // 0b01000000 -> 1,1,1
                // 0b00100000 -> 0,1,1
                // 0b00010000 -> 0,1,0
                // 0b00001000 -> 1,0,0
                // 0b00000100 -> 1,0,1
                // 0b00000010 -> 0,0,1
                // 0b00000001 -> 0,0,0
            }

            x_idx.push(y_idx);
        }
        idx.push(x_idx);
    }

    commands.spawn((
        MarchedCube,
        Mesh3d(meshes.add(cube_idx_to_mesh(idx))),
        MeshMaterial3d(materials.add(Color::WHITE)),
        WireframeObject,
    ));
}

